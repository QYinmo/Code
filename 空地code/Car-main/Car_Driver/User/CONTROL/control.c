		// Wheel (motor) target speed limit //????(???)?????????
		MotorA.Target_Encoder = target_limit_float(MotorA.Target_Encoder, -amplitude, amplitude);
		MotorB.Target_Encoder = target_limit_float(MotorB.Target_Encoder, -amplitude, amplitude);
